public class NumAndCount {
    int num;
    int count;


    public NumAndCount() {}
    public NumAndCount(int num, int count) {
        this.num = num;
        this.count = count;
    }

    @Override
    public String toString() {
        return "NumAndCount{" +
                "num=" + num +
                ", count=" + count +
                '}';
    }
}
